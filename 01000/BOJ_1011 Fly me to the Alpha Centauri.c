// https://www.acmicpc.net/problem/1011
#include <stdio.h>
#include <math.h>

void solution(int x)
{
    int k = sqrt(x);
    
    for(int i = k; i <= x; ++i)
    {
        if(x <= (long long)i*(i+1))
        {
            if(x <= (long long)i*i) printf("%d\n", 2 * i - 1);
            else printf("%d\n", 2 * i);
            return;
        }
    }
}

int main()
{
    int n;
    scanf("%d", &n);

    for(int i = 0; i < n; ++i)
    {
        int x, y;
        scanf("%d %d", &x, &y);
        solution(y - x);
    }
}


